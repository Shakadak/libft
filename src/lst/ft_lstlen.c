#include "inc/lst.h"

size_t	ft_lstlen(const t_lst *lst)
{
	const t_lst	*l;
	size_t		i;

	i = 0;
	l = lst;
	while (l)
	{
		i += 1;
		l = l->next;
	}
	return (i);
}
